import random

def get_vowels(line, no):
    vowels = ""
    for letter in line.lower()[::-1]:
        if letter in "aáeéiíoóöőuúüű":
            vowels = vowels + letter
            if vowels.__len__() > no-1:
                break
    return vowels[::-1]

def compare(line1, line2):
    i = 1
    while get_vowels(line1, i) == get_vowels(line2, i) and get_vowels(line1, i).__len__() == i:
        i += 1
    return i-1

